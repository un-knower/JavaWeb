package model;

public class Encode
{
	private static String encode="";
	private static String URLHead="";
	private static String fil="";
	public static String getEncode()
	{
		return encode;
	}

	public static void setEncode(String encode)
	{
		Encode.encode = encode;
	}

	public static String getURLHead()
	{
		return URLHead;
	}

	public static void setURLHead(String uRLHead)
	{
		URLHead = uRLHead;
	}

	public static String getFil()
	{
		return fil;
	}

	public static void setFil(String fil)
	{
		Encode.fil = fil;
	}

	
}
